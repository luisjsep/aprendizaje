

'''
script de python que calcul el área de un triángulo. El usuario deberá ingresar tanto
el valor de la base como el de la altura y el programa mostrará el valor del
área
'''

print ('Programa para calcular el área de un triángulo')
base= float(input('Base: '))
altura= float(input('Altura: '))

area= base * altura / 2

print(f'El área es igual a {area}')
